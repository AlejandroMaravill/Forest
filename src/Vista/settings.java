    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        // TODO add your handling code here:
        String name = "sysconfig.txt";
        if (txtip.getText().trim().equals("")||txtatabase.getText().trim().equals("")||txtport.getText().trim().equals("")||txtuser.getText().trim().equals("")||txtclave.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Algunos campos requeridos no han sido completados, por favor revise la informacion");
        }else {
            Controlador.ConfiguracionControlador.ip = txtip.getText();
            Controlador.ConfiguracionControlador.database = txtatabase.getText();
            Controlador.ConfiguracionControlador.port = txtport.getText();
            Controlador.ConfiguracionControlador.user = txtuser.getText();
            Controlador.ConfiguracionControlador.password = txtclave.getText();
            JOptionPane.showMessageDialog(null, "Se han realizado los cambios exitosamente");
            creararchivo();
            ocultar(name);
            Login open = new Login();
            open.setVisible(true);
            dispose();
        }
        
    }//GEN-LAST:event_btnAceptarMouseClicked

