    void creararchivo(){
        try {
            ProcessBuilder builder = new ProcessBuilder(
            "cmd.exe", "/c", "cd");
            builder.redirectErrorStream(true);
            Process p = builder.start();
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            String line;
            while (true) {
                line = r.readLine();
                if (line == null) { break; }
                try {
                    PrintWriter print = new PrintWriter(line + "/sysconfig.txt");
                    print.println(txtip.getText());
                    print.println(txtport.getText());
                    print.println(txtatabase.getText());
                    print.println(txtuser.getText());
                    print.println(txtclave.getText());
                    print.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        } catch (Exception e) {
            System.out.println("mal");
        }
    }
    
    public static void ocultar(String name){
        try {
            Process process = Runtime.getRuntime().exec("cmd /C start /B /wait attrib " + name + " +h");
        } catch (IOException ioe) {
            System.out.println (ioe);
        }
    }
    
    public static void leer(){      
        try {
            ProcessBuilder builder = new ProcessBuilder(
            "cmd.exe", "/c", "cd");
            builder.redirectErrorStream(true);
            Process p = builder.start();
            BufferedReader r = new BufferedReader(new InputStreamReader(p.getInputStream()));
            String line;
            while (true) {
                line = r.readLine();
                if (line == null) { break; }
                try {
                    Scanner input = new Scanner(new File(line + "/sysconfig.txt"));
                    int i = 0;
                    while (input.hasNextLine()) {                
                        String space = input.nextLine();
                        switch(i){
                            case 0:
                                ConfiguracionControlador.ip = space;
                                break;
                            case 1:
                                ConfiguracionControlador.port = space;
                                break;
                            case 2:
                                ConfiguracionControlador.database = space;
                                break;
                            case 3:
                                ConfiguracionControlador.user = space;
                                break;
                            case 4:
                                ConfiguracionControlador.password = space;
                                break;
                            default:
                                JOptionPane.showMessageDialog(null, "Error en la lectura");
                                break;
                        }
                        i++;
                    }
                    input.close();
                } catch (Exception e) {
                    System.out.println("mal");
                }
            }
        } catch (Exception e) {
            System.out.println("mal");
        }
    }
    
    
